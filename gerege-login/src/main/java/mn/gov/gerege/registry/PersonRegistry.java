package mn.gov.gerege.registry;

/**
 * Иргэний бүртгэлээс мэдээлэл татах гэрээ.
 *
 * <p>Энэ нь нэвтрэлтийн аргаас (Mobile-ID г.м.) ТУСДАА байх нь зарчмын чухал.
 * Нэвтрэлт нь "чи түлхүүрээ эзэмшиж байна уу" гэдгийг батална; харин "тэр РД хэн
 * бэ" гэдгийг иргэний бүртгэл (Монголд ХУР/УБЕГ) хариуцна. TARA мөн ингэж X-Road-аар
 * нэрийг тусад нь татдаг.</p>
 */
public interface PersonRegistry {

    /**
     * Регистрийн дугаараар иргэний мэдээлэл татах.
     *
     * @param personalCode РД
     * @return иргэн, эсвэл олдоогүй бол {@code null}
     */
    Person lookup(String personalCode);
}
